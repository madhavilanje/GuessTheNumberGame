    private void txtuserguessActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtuserguessActionPerformed
        // TODO add your handling code here:
        if ((Integer.parseInt(txtuserguess.getText())== randomNumber)){
            
            JOptionPane.showMessageDialog(null,"Congratulations, You won!"+"\nWith "+guessCount+" guesses.");
            txtuserguess.setText("");
            guessCount=0;
            txtguesscounter.setText("Guesses: "+guessCount);
            
        }
        else if ((Integer.parseInt(txtuserguess.getText())> randomNumber)){
            JOptionPane.showMessageDialog(null,"Entered number is greater");
            txtuserguess.setText("");
            
        }
        else if ((Integer.parseInt(txtuserguess.getText())< randomNumber)){
            JOptionPane.showMessageDialog(null,"Entered number is smaller");
            txtuserguess.setText("");
        }
        else if (guessCount==3){
            JOptionPane.showMessageDialog(null,"Number of guesses exhausted.\nGame Over1");
            txtuserguess.setText("");
            
        }
        guessCount++;
        txtguesscounter.setText("Guesses: "+guessCount);
    }//GEN-LAST:event_txtuserguessActionPerformed

