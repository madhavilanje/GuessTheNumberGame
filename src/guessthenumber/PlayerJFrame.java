    void checkGuess(ActionEvent event){
        if ((Integer.parseInt(txtuserguess.getText())== randomNumber)){
            
            JOptionPane.showMessageDialog(null,"Congratulations, You won!");
        }
        else if ((Integer.parseInt(txtuserguess.getText())> randomNumber)){
            JOptionPane.showMessageDialog(null,"Entered number is greater");
        }
        else if ((Integer.parseInt(txtuserguess.getText())< randomNumber)){
            JOptionPane.showMessageDialog(null,"Entered number is smaller");
        }
        guessCount++;
        txtguesscounter.setText("Guesses: "+guessCount);
    }
